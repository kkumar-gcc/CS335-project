public class test_3 {
    // Program with conditional statements:
    public static void main(String[] args) {
        int num = 10;
        if (num > 0) {
            System.out.println(num + " is a positive number");
        } else if (num < 0) {
            System.out.println(num + " is a negative number");
        } else {
            System.out.println(num + " is zero");
        }
    }
}
